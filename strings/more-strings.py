print("This is Alice's cat")
print('This is Alice\'s cat')

# raw string
print(r'Hello')
print(r'That is Carol\'s cat.')

# multi line string
text = """Dear Alice,
Eve's cat has been a real menace lately.
Please get your cat under control.
"""
print(text)

# indexes and slices are list-like
spam = 'hello world'
print(spam[0])
print(spam[2:5])
print(spam[-1])
print('hello' in spam)
print('x' in spam)
print('HELLO' in spam)

